    <div class="content">
        <h2 class="left-aligned" style="color: #682286;"><b>Karakteristik<br></h2></b>
        <p>Pemikir Kritis dan Strategis: INTJ memiliki pola pikir yang terstruktur dan selalu mencari cara 
            untuk memecahkan masalah dengan efisien. Mereka cenderung berpikir jauh ke depan dan suka merencanakan setiap langkah dengan teliti.
        </p>
        <p>
          Mandiri dan Berprinsip: Mereka menghargai independensi dan kemandirian, serta sering kali memiliki prinsip yang kuat yang jarang digoyahkan.
